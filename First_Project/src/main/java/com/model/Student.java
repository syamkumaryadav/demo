package com.model;

//POJO--Plain Oriented java object    (may be)

public class Student {

	

	    private String sno;
	    private String sname;
	    private String sage;
	    private String scourse;

	    // Getter for sno
	    public String getSno() {
	        return sno;
	    }

	    // Setter for sno
	    public void setSno(String sno) {
	        this.sno = sno;
	    }

	    // Getter for sname
	    public String getSname() {
	        return sname;
	    }

	    // Setter for sname
	    public void setSname(String sname) {
	        this.sname = sname;
	    }

	    // Getter for sage
	    public String getSage() {
	        return sage;
	    }

	    // Setter for sage
	    public void setSage(String sage) {
	        this.sage = sage;
	    }

	    // Getter for scourse
	    public String getScourse() {
	        return scourse;
	    }

	    // Setter for scourse
	    public void setScourse(String scourse) {
	        this.scourse = scourse;
	    }


	}


